                <h2>The Fun Perks</h2>
                <img src="https://i.postimg.cc/qMs9HrLD/May4.jpg" alt="So Pretty" width="250" style="float:left">
                <p>Working at Disney World comes with a few really fun perks. First and probably most obvious is free park parking and admission. If you mannaged to snag yourself
                    a fancy park reservation you could go to the parks. The Grand Floridian was a short monorail or boat ride away to Magic Kingdom, so sometimes I would go and 
                    "play" in the park (yes this is the offical Disney term for a Cast Member going to the parks on their time off) before or after work depending on when my shift was.
                </p>
                <p>My favorite day I got to go in the parks was May 4th aka Star Wars Day. My friend and I weren't able to get park passes for Hollywood Studios (where the Star Wars 
                    Land was located) so we went to Magic Kingdom until we were able to park hop. Yes I wore those heavy Sith Robes all day in the Florida heat. But the look on 
                    the Magic Kingdom's Cast Members faces' was worth commiting to the bit.  
                </p>
